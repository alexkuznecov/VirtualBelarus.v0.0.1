package by.grsu.domain;

public class SceneImage {

    private Integer id;

    private Image image;

    private Integer scenaId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Image getImage() {
        return image;
    }

    public void setImage(Image image) {
        this.image = image;
    }

    public Integer getScenaId() {
        return scenaId;
    }

    public void setScenaId(Integer scenaId) {
        this.scenaId = scenaId;
    }
}
